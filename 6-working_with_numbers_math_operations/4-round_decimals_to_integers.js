/// Round Decimals to Integers
// 1.234 -> 1

// 1- floor
var result1 =Math.floor(1.234);
console.log(result1);


// 2- ceil
var result2 =Math.ceil(1.234);
console.log(result2); /// 2

// 3- round
var result3 =Math.round(1.234);
console.log(result3); /// 1


/// 4- negative integer
console.log(Math.floor(-1.234));